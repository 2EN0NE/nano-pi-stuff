export const FOX_STATES = Object.keys(ANIMS) as FoxState[];

const paletteKeys = new Set(Object.keys(PALETTE));

for (const [state, animation] of Object.entries(ANIMS)) {
	for (const [frameIndex, frame] of animation.grids.entries()) {
		if (frame.length !== FOX_HEIGHT) {
			throw new Error(
				`${state} frame ${frameIndex} has ${frame.length} rows; expected ${FOX_HEIGHT}`,
			);
		}
		for (const [rowIndex, row] of frame.entries()) {
			if (row.length !== FOX_WIDTH) {
				throw new Error(
					`${state} frame ${frameIndex} row ${rowIndex} has ${row.length} pixels; expected ${FOX_WIDTH}`,
				);
			}
			for (const pixel of row) {
				if (pixel !== '.' && !paletteKeys.has(pixel)) {
					throw new Error(
						`${state} frame ${frameIndex} row ${rowIndex} uses unknown pixel ${pixel}`,
					);
				}
			}
		}
	}
}
